public class Integer {

    public static void main(String[] args) {
        
        int age = 20;
        int yearsToAdd = 5;
        int futureAge = age + yearsToAdd;

        System.out.println("Current Age: " + age);
        System.out.println("Years to Add: " + yearsToAdd);
        System.out.println("Age After 5 Years: " + futureAge);
    }
}